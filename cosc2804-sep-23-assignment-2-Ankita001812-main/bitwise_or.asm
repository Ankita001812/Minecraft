.ORIG x3000
TRAP 0x31



ADD R0, R0, #1
ADD R1, R1, #-1
; GETTING FIRST BLOCK
TRAP 0x33
; 1ST BLOCK STORING IT IN R4
ADD R4, R3, #0
TRAP 0x36



ADD R0, R0, #1
; GETTING 2ND BLOCK
TRAP 0x33
; 2ND BLOCK STORING IT IN R4
ADD R5, R3, #0
TRAP 0x36


; OR 
NOT R4, R4
NOT R5, R5
AND R6, R5, R4
NOT R6, R6


; COPYING THE VALUE TO R3 FOR THE TRAP
AND R3, R3, #0
; ADD R3, R6, #0
; SETTING THE BLOCK
ADD R0, R0, #1

STI R6, BLOCK_ID

LDI R3, BLOCK_ID

; SETTING THE BLOCK trap
TRAP 0x34
AND R4, R4, #0
AND R5, R5, #0

TRAP 0x36
HALT

BLOCK_ID .FILL x3100

.END